/*
在C语言中, 数组声明时使用变量作为长度, 这在C99标准之前是不允许的。
    从C99标准开始, C语言引入了变长数组（Variable Length Arrays，简称VLA）的概念,允许使用变量来指定数组的长度.

变长数组（VLA）的特性：
1. 声明位置: 变长数组必须在函数内部声明(即局部变量), 不能在文件作用域(全局)或静态存储期（如使用`static`关键字）的数组中使用;
2. 长度变量: 数组长度必须是自动存储期的整型变量(如局部变量), 且该变量在数组声明时必须已经定义并赋值(可以是运行时计算的值);
3. 不能初始化: 变长数组在声明时不能进行初始化(因为长度在运行时确定, 编译器无法在编译时分配空间并初始化);
4. 内存分配: 变长数组在栈上分配内存, 因此如果数组很大, 可能会导致栈溢出;
5. 可移植性: C11标准中, 变长数组变成了一个可选特性(编译器可以选择是否支持);
    因此, 为了可移植性, 应避免使用变长数组, 或者确保编译器支持.
*/