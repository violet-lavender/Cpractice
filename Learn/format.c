/* 格式化输出:
基本格式符:
格式符	        说明	                    示例
%d	        十进制整数（int）	        printf("%d", 42);
%u	        无符号十进制整数	        printf("%u", 100u);
%o	        八进制整数	                printf("%o", 10); → 12
%x/%X	    十六进制整数（小写/大写）    printf("%x", 255); → ff
%f	        浮点数（float或double）	    printf("%f", 3.14);
%e/%E	    科学计数法（小写e/大写E）	 printf("%e", 123.45);
%g/%G	    自动选择%f或%e（更简洁格式） printf("%g", 0.0001);
%c	        单个字符	                printf("%c", 'A');
%s	        字符串	                    printf("%s", "Hello");
%p	        指针地址（十六进制）	     printf("%p", &var);
%%	        输出%符号本身	printf("%%"); → %

格式修饰符:
修饰符	        说明	                        示例
%5d	        最小宽度为5（右对齐，空格填充）	  printf("%5d", 10); → "   10"
%-5d	    左对齐	                        printf("%-5d", 10); → "10   "
%05d	    用0填充空白	                    printf("%05d", 10); → "00010"
%.2f	    保留2位小数	                    printf("%.2f", 3.1415); → 3.14
%*.*f	    动态指定宽度和精度（通过参数传递）printf("%*.*f", 5, 2, 3.14); -> " 3.14"
%+d	        显示正负号	                    printf("%+d", 10); → "+10"
%#x	        显示进制前缀（如0x）	         printf("%#x", 255); → 0xff
*/

/*格式化输入:
基本格式符:
格式符	        说明	                        示例
%d	        读取十进制整数（int）	        scanf("%d", &num);
%u	        读取无符号十进制整数	        scanf("%u", &unum);
%o	        读取八进制整数	                scanf("%o", &oct);
%x	        读取十六进制整数	            scanf("%x", &hex);
%f/%lf	    读取float（%f）或double（%lf）  scanf("%f", &fval);
%c	        读取单个字符（包括空格）	    scanf("%c", &ch);
%s	        读取字符串（跳过空白，直到空白结束）scanf("%s", str);
%[]	        扫描字符集合（如%[a-z]读取小写字母）scanf("%[^\n]", line);(读取所有字符包括空格直至换行符)

格式修饰符:
修饰符	        说明	                    示例
%10s	       限制输入长度（防止溢出）	scanf("%10s", str);
%*d	           跳过输入（不保存值）	    scanf("%*d %d", &num);
*/